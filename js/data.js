function getitems(cat, index, val, rrange, vrange) {
	const arr = [];
	while(arr.length < 3) {
		const rand = (rrange && Math.random() < 0.3) ? val + (Math.random() < 0.5 && val >= rrange ? -rrange : rrange) : data[cat][Math.floor(Math.random() * data[cat].length)][index];
		if(!arr.includes(rand) && rand !== val && (!rrange || Math.abs(val - rand) <= vrange || Math.random() < 0.5)) arr.push(rand);
	}
	return arr;
}